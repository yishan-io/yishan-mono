/**
 * Mobile copy remains centralized here for now because large parts of the vocabulary
 * intentionally mirror desktop strings via `desktopValue(...)`.
 * Split only when a namespace boundary improves feature ownership rather than creating
 * mechanical duplication.
 */
type CopyLeaf = string;
type CopyTree = { [key: string]: CopyLeaf | CopyTree };
type DesktopTree = { [key: string]: string | DesktopTree };

const desktopCopy = {
  en: desktopEn as DesktopTree,
  zh: desktopZh as DesktopTree,
} satisfies Record<AppLanguagePreference, DesktopTree>;

function resolveTreeValue(tree: CopyTree | DesktopTree, key: string): CopyLeaf | undefined {
  const value = key.split(".").reduce<CopyLeaf | CopyTree | DesktopTree | undefined>((current, segment) => {
    if (!current || typeof current === "string") {
      return undefined;
    }

    return current[segment];
  }, tree);

  return typeof value === "string" ? value : undefined;
}

function desktopValue(preference: AppLanguagePreference, key: string, fallback: string): string {
  return resolveTreeValue(desktopCopy[preference], key) ?? fallback;
}

